# Operadores

operandoA = 3
operandoB = 2
suma = operandoA + operandoB
print(f"Resultado de suma: {suma}")

resta = operandoA - operandoB
print(f'Resultado de resta: {resta}')

multiplicacion = operandoA * operandoB
print(f'Resultado de la multiplicación: {multiplicacion}')

division = operandoA / operandoB
print(f'Resultado división float: {division}')

divIntera = operandoA // operandoB
print(f'Resultado div int: {divIntera}')

modulo = operandoA % operandoB
print(f'Resultado módulo: {modulo}')

exponente = operandoA ** operandoB
print(f'Resultado de exponente: {exponente}')