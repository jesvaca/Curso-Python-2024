a = 4
b = 2

resultado = a == b
print(f'Resultado igual que == : {resultado}')

resultado = a != b
print(f'Resultado diferente que != : {resultado}')

resultado = a > b
print(f'Resultado mayor que  > : {resultado}')

resultado = a < b
print(f'Resultado de menor que < {resultado}')

resultado = a >= b
print(f'Resultado de mayor igual que >= : {resultado}')

resultado = a <= b
print(f'Resultado de mayor igual que <= : {resultado}')